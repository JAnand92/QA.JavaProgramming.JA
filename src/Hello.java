public class Hello {
    /* Some basic tips about java programming language
    * Java is case sensitive object-oriented programming language
    * ie. PUBLIC and public both has different meaning in java language
    * Here, public is Access Modifier i.e. it gives access to the class
    * to get it accessible from other class or other software code.
    */

    public static void main (String[] args){
        /* Public is access modifier
        * As this main method is self initiated and not referenced by any object
        * hence, static keyword is added.
        * Void is java keyword which means this method will not return anything.
        */
        System.out.println("Hello, Jayesh!!");
    }
}
